package cmm02.op;

public class No01_DanHangDemo {
	public static void main(String[] args) {
		int i = 5;
		i = i + 1;
		System.out.println("i = i + 1 의 결과값 : " + i);
		
		int j = 5;
		j++;
		System.out.println("j++ 의  결과값 : " + j);
		
		int k = 5;
		++k;
		System.out.println("++k의 결과값 : " + k);
	}
}
